"use client";

import { useAuthActions } from "@convex-dev/auth/react";
import Link from "next/link";
import { usePathname, useRouter } from "next/navigation";
import { AppLogo } from "@/components/foundations/logo/app-logo";
import { Button } from "@/components/base/buttons/button";

const adminNavItems = [
  { href: "/admin", label: "Dashboard", icon: ChartIcon },
  { href: "/admin/organizations", label: "Organizations", icon: BuildingIcon },
  { href: "/admin/users", label: "Users", icon: UsersIcon },
  { href: "/admin/logs", label: "Audit Logs", icon: ListIcon },
];

export function AdminSidebar() {
  const pathname = usePathname();
  const router = useRouter();
  const { signOut } = useAuthActions();

  return (
    <aside className="flex flex-col w-64 bg-utility-gray-900 min-h-screen">
      <div className="flex items-center h-16 px-4 border-b border-utility-gray-800">
        <div className="flex items-center gap-3">
          <AppLogo variant="white" className="h-8" />
          <span className="text-sm font-semibold text-white">Admin</span>
        </div>
      </div>

      <nav className="flex-1 px-3 py-4 space-y-1">
        {adminNavItems.map((item) => {
          const isActive =
            pathname === item.href ||
            (item.href !== "/admin" && pathname.startsWith(item.href));
          const Icon = item.icon;

          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-colors ${
                isActive
                  ? "bg-utility-gray-800 text-white"
                  : "text-utility-gray-400 hover:bg-utility-gray-800 hover:text-white"
              }`}
            >
              <Icon className="w-5 h-5" />
              {item.label}
            </Link>
          );
        })}
      </nav>

      <div className="p-4 border-t border-utility-gray-800 space-y-2">
        <Link href="/dashboard">
          <Button color="secondary" size="sm" className="w-full justify-start">
            <ArrowLeftIcon className="w-4 h-4 mr-2" />
            Back to app
          </Button>
        </Link>
        <Button
          color="tertiary"
          size="sm"
          className="w-full justify-start text-utility-gray-400"
          onClick={async () => {
            await signOut();
            router.push("/login");
          }}
        >
          <LogOutIcon className="w-4 h-4 mr-2" />
          Log out
        </Button>
      </div>
    </aside>
  );
}

