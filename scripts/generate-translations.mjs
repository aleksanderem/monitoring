// Files that already have manual translations - skip these
const skipFiles = new Set(['auth', 'nav', 'common', 'jobs', 'share', 'generators', 'aiResearch']);

function translateValue(value, dict) {
  if (typeof value !== 'string') return value;

  // Skip very long values (moduleBenefit* etc) - keep EN
  if (value.length > 500) return value;

  // Try exact match first
  if (dict[value]) return dict[value];

  // Extract ICU placeholders before translation to protect them
  const placeholders = [];
  let result = value.replace(/\{[^}]+\}/g, (match) => {
    const idx = placeholders.length;
    placeholders.push(match);
    return `\x00PH${idx}\x00`;
  });

  // Try word-boundary replacements for shorter strings
  // Sort keys by length (longest first) for correct replacement order
  const sortedKeys = Object.keys(dict).sort((a, b) => b.length - a.length);

  for (const eng of sortedKeys) {
    if (result.includes(eng)) {
      // Only replace if it looks like a standalone word/phrase (not inside another word)
      const escaped = eng.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
      const regex = new RegExp(`(?<![a-zA-Z])${escaped}(?![a-zA-Z])`, 'g');
      result = result.replace(regex, dict[eng]);
    }
  }

  // Restore ICU placeholders
  result = result.replace(/\x00PH(\d+)\x00/g, (_, idx) => placeholders[parseInt(idx)]);

  return result;
}

function translateObject(obj, dict) {
  const result = {};
  for (const [key, value] of Object.entries(obj)) {
    if (typeof value === 'object' && value !== null && !Array.isArray(value)) {
      result[key] = translateObject(value, dict);
    } else {
      result[key] = translateValue(value, dict);
    }
  }
  return result;
}

const locales = ['de', 'es', 'fr'];
const enDir = path.join(messagesDir, 'en');
const namespaces = fs.readdirSync(enDir).filter(f => f.endsWith('.json')).map(f => f.replace('.json', ''));

for (const locale of locales) {
  const localeDir = path.join(messagesDir, locale);

  for (const ns of namespaces) {
    // Skip files that already have manual translations
    if (skipFiles.has(ns)) {
      if (fs.existsSync(path.join(localeDir, `${ns}.json`))) {
        continue; // Already exists
      }
    }

    const targetPath = path.join(localeDir, `${ns}.json`);

    // If file already exists and has content, skip
    if (fs.existsSync(targetPath)) {
      const existing = fs.readFileSync(targetPath, 'utf-8');
      if (existing.length > 10) continue;
    }

    const enData = JSON.parse(fs.readFileSync(path.join(enDir, `${ns}.json`), 'utf-8'));
    const translated = translateObject(enData, translations[locale]);
    fs.writeFileSync(targetPath, JSON.stringify(translated, null, 2) + '\n');
    console.log(`Generated ${locale}/${ns}.json`);
  }
}

